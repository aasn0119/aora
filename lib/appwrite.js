export const getFilePreview = async (fileId, type) => {
  let fileUrl;
  try {
    if (type === "video") {
      fileUrl = storage.getFileView(storageId, fileId);
      console.log("Video -> fileUrl :: ", fileUrl);
    } else if (type === "image") {
      fileUrl = storage.getFilePreview(
        storageId,
        fileId,
        2000,
        2000,
        "top",
        100
      );
      console.log("Image -> fileUrl :: ", fileUrl);
    } else {
      throw new Error("Invalid file type");
    }

    if (!fileUrl) throw new Error("No file found");

    return fileUrl;
  } catch (error) {
    console.error("getFilePreview error :: ", error); // Log the error
    throw new Error(error);
  }
};

export const uploadFile = async (file, type) => {
  if (!file) return;

  const { mimeType, ...rest } = file;
  const asset = {
    name: file.fileName,
    type: file.mimeType,
    size: file.fileSize,
    uri: file.uri,
  };

  try {
    const uploadedFile = await storage.createFile(
      storageId,
      ID.unique(),
      asset
    );

    console.log("uploaded :: ", uploadedFile);

    const fileUrl = await getFilePreview(uploadedFile.$id, type);
    console.log("fileUrl :: ", fileUrl);

    return fileUrl;
  } catch (error) {
    console.error("uploadFile error :: ", error); // Log the error
    throw new Error(error);
  }
};

export const createVideo = async (form) => {
  console.log("form :: ", form);
  try {
    const [thumbnailUrl, videoUrl] = await Promise.all([
      uploadFile(form.thumbnail, "image"),
      uploadFile(form.video, "video"),
    ]);

    console.log("thumbnailUrl :: ", thumbnailUrl);
    console.log("videoUrl :: ", videoUrl);

    const newPost = await databases.createDocument(
      databaseId,
      videoCollectionId,
      ID.unique(),
      {
        title: form.title,
        thumbnail: thumbnailUrl,
        video: videoUrl,
        prompt: form.prompt,
        creator: form.userId,
      }
    );

    return newPost;
  } catch (error) {
    console.error("createVideo error :: ", error); // Log the error
    throw new Error(error);
  }
};
