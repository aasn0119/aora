import {
  Account,
  Avatars,
  Client,
  Databases,
  ID,
  Query,
  Storage,
} from "react-native-appwrite";

export const config = {
  endpoint: "https://cloud.appwrite.io/v1",
  platform: "com.aasn.aora",
  projectId: "66c4b58100247fadb9a2",
  databaseId: "66c4b75c0028e4be01d5",
  userCollectionId: "66c4b77c000c064e1ac3",
  videoCollectionId: "66c4b7b5002577fa70b2",
  storageId: "66c4b990002f5ddde863",
};

const {
  endpoint,
  projectId,
  platform,
  databaseId,
  userCollectionId,
  videoCollectionId,
  storageId,
} = config;

const client = new Client();

client.setEndpoint(endpoint).setProject(projectId).setPlatform(platform);

const account = new Account(client);
const avatars = new Avatars(client);
const databases = new Databases(client);
const storage = new Storage(client);

